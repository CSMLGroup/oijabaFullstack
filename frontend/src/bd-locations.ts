function fallbackPostalCode(name: string): string {
  const code = 1000 + (Array.from(name).reduce((sum, ch) => sum + ch.charCodeAt(0), 0) % 9000)
  return String(code)
}

function buildPostOfficeList(upazilla: string): PostOfficeOption[] {
  const code = POSTAL_CODE_OVERRIDES[upazilla] || fallbackPostalCode(upazilla)
  return [
    { name: `${upazilla} Post Office`, code },
    { name: `${upazilla} Sadar Post Office`, code }
  ]
}

export const LOCATION_TREE: Record<string, Record<string, PostOfficeOption[]>> = Object.fromEntries(
  Object.entries(BD_LOCATIONS).map(([district, upazillas]) => [
    district,
    Object.fromEntries(upazillas.map((upazilla) => [upazilla, buildPostOfficeList(upazilla)]))
  ])
) as Record<string, Record<string, PostOfficeOption[]>>

export const POST_OFFICES: Record<string, PostOfficeOption[]> = Object.fromEntries(
  Object.values(LOCATION_TREE)
    .flatMap((upazillaMap) => Object.entries(upazillaMap))
    .map(([upazilla, offices]) => [upazilla, offices])
) as Record<string, PostOfficeOption[]>
